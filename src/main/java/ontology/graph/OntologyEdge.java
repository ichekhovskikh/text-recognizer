package ontology.graph;

public class OntologyEdge {
    private String text;

    public OntologyEdge(String text) {
        this.text = text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getText() {
        return text;
    }

    @Override
    public String toString() {
        return text;
    }
}
